#include <iostream>

int main()
{
    int a = 30, b = 4;
    
    std::cout << "a + b = " << a + b << "\n";
    std::cout << "a - b = " << a - b << "\n";
    std::cout << "a * b = " << a * b << "\n";
    std::cout << "a / b = " << (double)a / b << "\n";
    std::cout << "a % b = " << a % b << "\n";

    return 0;
}
